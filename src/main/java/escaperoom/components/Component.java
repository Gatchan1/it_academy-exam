package escaperoom.components;

public abstract class Component {
    protected int id;
    protected String name;

    public Component(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public abstract String toString();
}
